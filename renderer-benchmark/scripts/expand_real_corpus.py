@dataclass
class Downloaded:
    id: str
    path: Path
    category: str
    source: str
    source_url: str
    license: str
    license_url: str
    notes: str
    sha256: str
    size_bytes: int


def request_bytes(url: str, timeout: int = 60) -> bytes:
    req = urllib.request.Request(url, headers={"User-Agent": USER_AGENT})
    with urllib.request.urlopen(req, timeout=timeout) as response:
        return response.read()


def safe_stem(name: str) -> str:
    stem = Path(name).stem
    return re.sub(r"[^A-Za-z0-9_.-]+", "_", stem).strip("_") or "pdf"


def classify_pdfjs(path: str) -> str:
    lower = path.lower()
    if any(token in lower for token in ("arabic", "hebrew", "bidi", "rtl", "thuluth")):
        return "real-rtl-text"
    if any(token in lower for token in ("cjk", "sjis", "rksj", "jis", "japan", "japanese", "chinese", "korea", "cmap", "cidfont", "identitytounicode", "simsun", "simfang", "xiaobiaosong", "eucjp")):
        return "real-cjk-text"
    if any(token in lower for token in ("jpx", "jp2", "jpeg2000", "jp2k")):
        return "real-jpeg2000"
    if any(token in lower for token in ("encrypt", "password", "protected", "sechandler", "permission")):
        return "real-encrypted"
    if any(token in lower for token in ("acro", "annot", "annotation", "button", "checkbox", "choice", "field", "form", "textfield", "xfa", "widget", "160f", "f1040")):
        return "real-forms"
    if any(token in lower for token in ("image", "scan", "ccitt", "jbig", "fax", "mask", "jpeg", "jpg", "png", "tiff")):
        return "real-scanned"
    if any(token in lower for token in ("shading", "gradient", "pattern", "transparen", "blend", "smask", "knockout", "type3", "tiling", "mesh")):
        return "real-complex-vector"
    if any(token in lower for token in ("column", "tracemonkey", "review", "openoffice", "paragraph", "wikipedia", "freeculture")):
        return "real-multi-column"
    if any(token in lower for token in ("font", "type1", "truetype", "opentype", "glyph")):
        return "real-font-edge"
    return "real-pdfjs-general"


def classify_irs(name: str) -> str:
    lower = name.lower()
    if lower.startswith("i") or lower.startswith("p"):
        return "real-multi-column"
    return "real-forms"


def download_pdf(url: str, destination: Path, expected_sha: str | None = None) -> tuple[str, int]:
    destination.parent.mkdir(parents=True, exist_ok=True)
    if destination.exists():
        data = destination.read_bytes()
        digest = hashlib.sha256(data).hexdigest()
        if expected_sha is None or digest == expected_sha:
            return digest, len(data)

    data = request_bytes(url)
    digest = hashlib.sha256(data).hexdigest()
    if expected_sha is not None and digest != expected_sha:
        raise ValueError(f"sha256 mismatch for {url}: expected {expected_sha}, got {digest}")
    destination.write_bytes(data)
    return digest, len(data)


def pdfjs_entries(limit: int | None = None) -> list[Downloaded]:
    tree = json.loads(request_bytes(PDFJS_TREE_URL).decode("utf-8"))
    blobs = [
        item
        for item in tree.get("tree", [])
        if item.get("type") == "blob"
        and str(item.get("path", "")).startswith("test/pdfs/")
        and str(item.get("path", "")).lower().endswith(".pdf")
    ]
    blobs.sort(key=lambda item: str(item["path"]).lower())
    if limit is not None:
        blobs = blobs[:limit]

    out: list[Downloaded] = []
    for item in blobs:
        source_path = str(item["path"])
        rel_name = source_path.removeprefix("test/pdfs/")
        url = PDFJS_RAW_PREFIX + quote(source_path, safe="/")
        destination = REAL_ROOT / "pdfjs-full" / rel_name
        # GitHub tree entries expose the Git blob SHA-1, not a file SHA-256.
        # Store our own SHA-256 in the sidecar metadata after download.
        try:
            sha, size = download_pdf(url, destination)
        except (urllib.error.HTTPError, urllib.error.URLError, TimeoutError) as err:
            print(f"skip pdf.js {rel_name}: {err}")
            continue
        out.append(
            Downloaded(
                id="pdfjs_full_" + safe_stem(rel_name),
                path=destination,
                category=classify_pdfjs(rel_name),
                source="mozilla/pdf.js test/pdfs full corpus",
                source_url=url,
                license="Apache-2.0",
                license_url=PDFJS_LICENSE_URL,
                notes="Expanded pdf.js renderer regression corpus fixture.",
                sha256=sha,
                size_bytes=size,
            )
        )
    return out


def irs_entries() -> list[Downloaded]:
    out: list[Downloaded] = []
    for name in IRS_PDFS:
        url = IRS_BASE + name
        destination = REAL_ROOT / "irs-public-domain" / name
        try:
            sha, size = download_pdf(url, destination)
        except (urllib.error.HTTPError, urllib.error.URLError, TimeoutError) as err:
            print(f"skip IRS {name}: {err}")
            continue
        out.append(
            Downloaded(
                id="irs_" + safe_stem(name),
                path=destination,
                category=classify_irs(name),
                source="U.S. Internal Revenue Service forms/publications",
                source_url=url,
                license="U.S. federal government public domain",
                license_url="https://www.usa.gov/government-copyright",
                notes="Public IRS PDF used for real-world form/document coverage.",
                sha256=sha,
                size_bytes=size,
            )
        )
    return out


def nist_entries() -> list[Downloaded]:
    out: list[Downloaded] = []
    for rel in NIST_PDFS:
        url = NIST_BASE + rel
        name = Path(rel).name
        destination = REAL_ROOT / "nist-public-domain" / name
        try:
            sha, size = download_pdf(url, destination)
        except (urllib.error.HTTPError, urllib.error.URLError, TimeoutError) as err:
            print(f"skip NIST {rel}: {err}")
            continue
        out.append(
            Downloaded(
                id="nist_" + safe_stem(name),
                path=destination,
                category="real-multi-column",
                source="NIST publications",
                source_url=url,
                license="U.S. federal government public domain",
                license_url="https://www.nist.gov/open/copyright-fair-use-and-licensing-statements-srd-data-and-software",
                notes="NIST public technical publication used for long-form real-world document coverage.",
                sha256=sha,
                size_bytes=size,
            )
        )
    return out


def to_metadata(entry: Downloaded) -> dict[str, Any]:
    return {
        "id": entry.id,
        "path": str(entry.path.relative_to(REPO_ROOT)).replace("\\", "/"),
        "category": entry.category,
        "source": entry.source,
        "source_url": entry.source_url,
        "license": entry.license,
        "license_url": entry.license_url,
        "notes": entry.notes,
        "sha256": entry.sha256,
        "size_bytes": entry.size_bytes,
    }


def write_metadata(entries: list[Downloaded]) -> None:
    data = {
        "version": 1,
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "description": "Source metadata for expanded Renderer Benchmark 0A real-world PDFs.",
        "entries": [to_metadata(entry) for entry in entries],
    }
    METADATA_PATH.parent.mkdir(parents=True, exist_ok=True)
    METADATA_PATH.write_text(json.dumps(data, indent=2) + "\n", encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--pdfjs-limit", type=int, help="download only the first N pdf.js PDFs for a smoke run")
    parser.add_argument("--skip-pdfjs", action="store_true")
    parser.add_argument("--skip-irs", action="store_true")
    parser.add_argument("--skip-nist", action="store_true")
    args = parser.parse_args()

    entries: list[Downloaded] = []
    if not args.skip_pdfjs:
        entries.extend(pdfjs_entries(args.pdfjs_limit))
    if not args.skip_irs:
        entries.extend(irs_entries())
    if not args.skip_nist:
        entries.extend(nist_entries())
    write_metadata(entries)

    by_category: dict[str, int] = {}
    for entry in entries:
        by_category[entry.category] = by_category.get(entry.category, 0) + 1
    print(json.dumps({"downloaded": len(entries), "by_category": by_category}, indent=2, sort_keys=True))


if __name__ == "__main__":
    main()
