pub struct Encoding;

impl Encoding {
    pub fn lookup(encoding_name: &str, byte: u8) -> &'static str {
        let table = match encoding_name {
            "StandardEncoding" | "Adobe-Standard" => &STANDARD_ENCODING,
            "WinAnsiEncoding" | "WinAnsi" => &WIN_ANSI_ENCODING,
            "MacRomanEncoding" | "MacRoman" => &MAC_ROMAN_ENCODING,
            "PDFDocEncoding" => &PDF_DOC_ENCODING,
            _ => return ".notdef",
        };
        table[byte as usize].unwrap_or(".notdef")
    }

    pub fn apply_differences(base: &str, differences: &[Operand]) -> Vec<String> {
        let mut table: Vec<String> = (0u8..=255)
            .map(|byte| Self::lookup(base, byte).to_string())
            .collect();
        let mut current_code: usize = 0;
        for item in differences {
            match item {
                Operand::Integer(n) if *n >= 0 => {
                    current_code = *n as usize;
                }
                Operand::Name(name) => {
                    if current_code < 256 {
                        table[current_code] = name.clone();
                    }
                    current_code = current_code.saturating_add(1);
                }
                _ => {}
            }
        }
        table
    }
}

