    #[test]
    fn differences_override_base_encoding() {
        let diffs = vec![
            Operand::Integer(0x27),
            Operand::Name("testsymbol".to_string()),
            Operand::Integer(0x60),
            Operand::Name("testaccent".to_string()),
        ];
        let table = Encoding::apply_differences("StandardEncoding", &diffs);
        assert_eq!(table[0x27], "testsymbol");
        assert_eq!(table[0x60], "testaccent");
        assert_eq!(table[0x41], "A");
    }

    #[test]
    fn agl_lookup_handles_common_names_and_unicode_names() {
        assert_eq!(glyph_name_to_unicode("A"), Some('A'));
        assert_eq!(glyph_name_to_unicode("emdash"), Some('\u{2014}'));
        assert_eq!(glyph_name_to_unicode("fi"), Some('\u{FB01}'));
        assert_eq!(glyph_name_to_unicode("quoteright"), Some('\u{2019}'));
        assert_eq!(glyph_name_to_unicode("Euro"), Some('\u{20AC}'));
        assert_eq!(glyph_name_to_unicode("uniABCD"), Some('\u{ABCD}'));
        assert_eq!(glyph_name_to_unicode("notaname"), None);
    }

